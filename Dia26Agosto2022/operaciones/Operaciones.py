def sumar(a:int|float, b:int|float)->int|float: #Se crea una funcion que pide como parametros un dato ya sea de tipo integer(int) o flotante(float) y retorna un valor ya sea tipo int o float
    return a + b #Se retorna el resultado de la suma entre los dos datos

def restar(a:int|float, b:int|float)->int|float: #Se crea una funcion que pide como parametros un dato ya sea de tipo integer(int) o flotante(float) y retorna un valor ya sea tipo int o float
    return a- b #Se retorna el resultado de la resta entre los dos datos

def multiplicar(a:int|float,b:int|float)->int|float: #Se crea una funcion que pide como parametros un dato ya sea de tipo integer(int) o flotante(float) y retorna un valor ya sea tipo int o float
    return a*b #Se retorna el resultado de la multiplicacion entre los dos datos

